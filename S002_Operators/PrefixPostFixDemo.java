package S002_Operators;

public class PrefixPostFixDemo {
    public static void main(String[] args) {
        int a = 10;

        System.out.printf("a = %d\n", a);
        System.out.printf("a++ = %d\n", a++);
        System.out.printf("a = %d\n", a);
        System.out.printf("++a = %d\n", ++a);
        System.out.printf("a = %d\n", a);
    }
}
